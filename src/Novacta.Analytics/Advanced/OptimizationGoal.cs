namespace Novacta.Analytics.Advanced
{
    /// <summary>
    /// Contains constants specifying if an objective
    /// function must be minimized or maximized.
    /// </summary>
    public enum OptimizationGoal
    {
        /// <summary>
        /// The objective function
        /// must be maximized.
        /// </summary>
        Maximization = 0,
        /// <summary>
        /// The objective function
        /// must be minimized.
        /// </summary>
        Minimization = 1
    }
}
